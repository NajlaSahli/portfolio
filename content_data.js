/* تحميل كلّ ملفّات المحتوى عند تحميل الصفحة */
(function loadAllContent() {
  if (typeof window === 'undefined' || !window.CONTENT_MANIFEST) return;
  window.CONTENT_LOADED = 0;
  window.CONTENT_MANIFEST.forEach(function(item) {
    var script = document.createElement('script');
    script.src = item.file;
    script.async = false;
    script.onload = function() {
      window.CONTENT_LOADED++;
      if (window.CONTENT_LOADED === window.CONTENT_MANIFEST.length) {
        document.dispatchEvent(new Event('content-ready'));
      }
    };
    document.head.appendChild(script);
  });
})();
